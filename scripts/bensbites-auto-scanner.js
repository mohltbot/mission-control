#!/usr/bin/env node
/**
 * Ben's Bites Auto-Scanner
 * Fetches latest newsletter, extracts tools, categorizes, and generates report
 * Runs: Wednesday and Friday mornings via cron
 */

const fs = require('fs');
const path = require('path');

const WORKSPACE = '/Users/mohlt/.openclaw/workspace';
const MEMORY_DIR = path.join(WORKSPACE, 'memory');
const LOGS_DIR = path.join(WORKSPACE, 'logs');

// Ensure directories exist
[MEMORY_DIR, LOGS_DIR].forEach(dir => {
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
});

const TODAY = new Date().toISOString().split('T')[0];
const TIMESTAMP = new Date().toISOString();

function log(message) {
    const line = `[${TIMESTAMP}] ${message}`;
    console.log(line);
    fs.appendFileSync(path.join(LOGS_DIR, 'bensbites-cron.log'), line + '\n');
}

async function fetchBensBites() {
    log('Fetching Ben\'s Bites newsletter...');
    
    // Try to fetch the latest post
    const urls = [
        'https://www.bensbites.com/p/google-apps-in-the-terminal',  // March 5 issue
        'https://www.bensbites.com',
    ];
    
    for (const url of urls) {
        try {
            const response = await fetch(url);
            if (response.ok) {
                const html = await response.text();
                log(`Successfully fetched: ${url}`);
                return { url, html };
            }
        } catch (err) {
            log(`Failed to fetch ${url}: ${err.message}`);
        }
    }
    
    throw new Error('Could not fetch Ben\'s Bites');
}

function extractTools(html) {
    log('Extracting tools from content...');
    
    const tools = [];
    
    // Look for links with tool names (common patterns in Ben's Bites)
    const linkRegex = /<a[^>]+href="([^"]+)"[^>]*>([^<]+)<\/a>/gi;
    let match;
    
    while ((match = linkRegex.exec(html)) !== null) {
        const url = match[1];
        const text = match[2].trim();
        
        // Filter for actual tool links (GitHub, product sites)
        if (url.includes('github.com') || 
            url.includes('.dev') || 
            url.includes('.ai') ||
            url.includes('.io') ||
            url.includes('raycast.com') ||
            url.includes('glideapps.com')) {
            
            // Skip if already added
            if (!tools.find(t => t.url === url)) {
                tools.push({
                    name: text,
                    url: url,
                    category: categorizeTool(text, url)
                });
            }
        }
    }
    
    log(`Extracted ${tools.length} potential tools`);
    return tools;
}

function categorizeTool(name, url) {
    const text = (name + ' ' + url).toLowerCase();
    
    // HIGH priority keywords
    if (text.includes('agent') || 
        text.includes('cli') || 
        text.includes('automation') ||
        text.includes('workflow') ||
        text.includes('infrastructure') ||
        text.includes('computer use') ||
        text.includes('parallel') ||
        text.includes('terminal')) {
        return 'HIGH';
    }
    
    // MEDIUM priority keywords
    if (text.includes('saas') || 
        text.includes('productivity') ||
        text.includes('design') ||
        text.includes('notes') ||
        text.includes('search')) {
        return 'MEDIUM';
    }
    
    return 'LOW';
}

function generateReport(tools, sourceUrl) {
    const highTools = tools.filter(t => t.category === 'HIGH');
    const mediumTools = tools.filter(t => t.category === 'MEDIUM');
    const lowTools = tools.filter(t => t.category === 'LOW');
    
    const report = `# Ben's Bites Auto-Scan - ${TODAY}
**Source:** ${sourceUrl}  
**Scan Date:** ${TODAY}  
**Status:** Auto-generated by cron job

---

## Summary

- **Total tools found:** ${tools.length}
- **HIGH priority:** ${highTools.length}
- **MEDIUM priority:** ${mediumTools.length}
- **LOW priority:** ${lowTools.length}

---

## 🔥 HIGH Priority Tools

${highTools.map(t => `### ${t.name}
**URL:** ${t.url}  
**Category:** HIGH
`).join('\n')}

---

## ⚖️ MEDIUM Priority Tools

${mediumTools.map(t => `- **${t.name}** - ${t.url}`).join('\n') || 'None found'}

---

## 📝 LOW Priority (Log Only)

${lowTools.map(t => `- ${t.name} - ${t.url}`).join('\n') || 'None found'}

---

*This report was auto-generated by the Ben's Bites scanner cron job.*
`;
    
    return report;
}

async function main() {
    log('=== Ben\'s Bites Auto-Scanner Starting ===');
    
    try {
        // Fetch the newsletter
        const { url, html } = await fetchBensBites();
        
        // Extract tools
        const tools = extractTools(html);
        
        // Generate report
        const report = generateReport(tools, url);
        
        // Save report
        const reportPath = path.join(MEMORY_DIR, `${TODAY}-bensbites-auto.md`);
        fs.writeFileSync(reportPath, report);
        log(`Report saved to: ${reportPath}`);
        
        // Also save to main bensbites file
        const mainPath = path.join(MEMORY_DIR, '2026-03-06-bensbites-scan.md');
        if (fs.existsSync(mainPath)) {
            fs.appendFileSync(mainPath, `\n\n---\n\n## Auto-Scan Appendix (${TODAY})\n\nAdditional tools detected by automated scan:\n\n${tools.map(t => `- ${t.name} (${t.category}): ${t.url}`).join('\n')}`);
            log(`Appended to main report: ${mainPath}`);
        }
        
        log('=== Scan Complete ===');
        
    } catch (err) {
        log(`ERROR: ${err.message}`);
        process.exit(1);
    }
}

main();
